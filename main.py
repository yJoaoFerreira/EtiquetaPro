# Este software deve cumprir as seguintes etapas para se considerar "completo":

# 1. Obter o diretório de um arquivo Excel;
# 2. Ler um arquivo Excel do diretório selecionado;
# 3. Obter informações do Excel no geral;
# 4. Manipular essas informações (usar regex se necessário);
# 5. Deixar as informações no molde de impressão;
# 6. Gerar um arquivo em PDF para impressão já nos moldes de etiqueta;
# 7. Estabelecer uma conexão da impressora com o programa;
# 8. Conectar-se com a impressora serial selecionada;
# 9. Enviar o PDF gerado para a impressora selecionada;
# 10. Imprimir o PDF enviado através do programa.